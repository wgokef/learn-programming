#include <iostream>
static double f(double x)
{
    if (x > 2)
    {
        return exp(x);
    }
    if (-2 <= x && x <= 2)
    {
        return x + 4;
    }
    if (x < -2)
    {
        return 0;
    }

    return 0;
}

int main()
{
        double start = -3;
        double end = 3;
        double step = 0.25;

        for (double x = start; x <= end; x += step)
        {
            std::cout << f(x);
        }
}

